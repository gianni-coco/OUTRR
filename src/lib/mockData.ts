const today = new Date();

// Helpers for exact dates
const tonight = (h: number, m = 0) => {
  const d = new Date(today);
  d.setHours(h, m, 0, 0);
  return d.toISOString();
};
const tomorrow = (h: number, m = 0) => {
  const d = new Date(today);
  d.setDate(d.getDate() + 1);
  d.setHours(h, m, 0, 0);
  return d.toISOString();
};
const daysFromNow = (days: number, h: number, m = 0) => {
  const d = new Date(today);
  d.setDate(d.getDate() + days);
  d.setHours(h, m, 0, 0);
  return d.toISOString();
};
// Spread events across months and year
const monthsFromNow = (months: number, day: number, h: number, m = 0) => {
  const d = new Date(today);
  d.setMonth(d.getMonth() + months, day);
  d.setHours(h, m, 0, 0);
  return d.toISOString();
};

